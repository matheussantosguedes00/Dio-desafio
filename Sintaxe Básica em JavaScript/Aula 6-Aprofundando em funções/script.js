//Tipos de função 

//declarativos
function funcao(){
    console.log('Sou uma mensagem de uma Função decalarativa ')
}
funcao();

// expressão de função
// com nomeação
var funcao=function funcao(){
    console.log('Sou uma mensagem de uma Função expressão')
}
funcao();
// sem nomeação
var funcao=function (){
    console.log('Sou uma mensagem de uma Função expressão  sem nomeação')
}
funcao();

//Arrow function
var funcao =()=>{
    console.log('Sou uma Arrow function')
}
funcao();
